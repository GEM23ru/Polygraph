    def _kb_save(items: list):
        try:
            with open(KB_PATH, "w", encoding="utf-8") as f:
                json.dump(items, f, ensure_ascii=False, indent=2)
        except Exception as e:
            print(f"[kb_save error] {e}")

    def remember(content: str, topic: str = "общее", tags: str = "", source: str = "") -> str:
        """Сохранить факт в личную базу знаний пользователя.
        content — что запомнить (короткое резюме, 50-500 слов лучше всего).
        topic — категория (например "wb-тарифы", "тренды-дизайн").
        tags — теги через запятую (для поиска).
        source — откуда (URL, имя файла, "из разговора")."""
        if not content or not content.strip():
            return "[remember] пустой content — нечего сохранять"
        items = _kb_load()
        # Простой id-счётчик
        new_id = f"k_{len(items)+1:04d}"
        # Парсим теги
        tag_list = [t.strip() for t in tags.split(",") if t.strip()]
        record = {
            "id": new_id,
            "ts": time.strftime("%Y-%m-%d %H:%M:%S"),
            "topic": topic.strip() or "общее",
            "tags": tag_list,
            "source": source.strip(),
            "content": content.strip(),
        }
        items.append(record)
        _kb_save(items)
        return (f"✅ Сохранено в базу знаний (id={new_id}, тема='{record['topic']}'). "
                f"Всего записей в базе: {len(items)}.")

    def recall(query: str, limit: int = 5) -> str:
        """Найти в базе знаний релевантные записи по запросу.
        Простой поиск по словам в content / topic / tags / source.
        Возвращает топ-N с самой высокой релевантностью."""
        items = _kb_load()
        if not items:
            return "[recall] база знаний пуста (используй remember чтобы сохранять)"
        q = (query or "").strip().lower()
        if not q:
            return "[recall] пустой запрос"
        # Слова запроса
        q_words = set(w for w in q.split() if len(w) >= 3)
        if not q_words:
            q_words = {q}  # fallback: один короткий запрос
        # Скоринг каждой записи
        scored = []
        for item in items:
            text = " ".join([
                item.get("content", ""),
                item.get("topic", ""),
                " ".join(item.get("tags") or []),
                item.get("source", ""),
            ]).lower()
            # +1 за каждое совпадение слова
            score = sum(1 for w in q_words if w in text)
            if score > 0:
                scored.append((score, item))
        if not scored:
            return f"[recall] по запросу '{query}' ничего не найдено в базе ({len(items)} записей)"
        # Сортируем по убыванию релевантности
        scored.sort(key=lambda x: -x[0])
        top = scored[:limit]
        lines = [f"📚 Из твоей базы знаний по запросу «{query}» ({len(scored)} совпадений, показано топ-{len(top)}):\n"]
        for score, item in top:
            tags_str = ", ".join(item.get("tags") or []) or "—"
            src = item.get("source") or "—"
            content = item.get("content", "")
            # Обрезаем длинный content
            if len(content) > 600:
                content = content[:600] + "..."
            lines.append(
                f"\n• [{item['id']}] {item.get('ts','')} · тема: {item.get('topic','?')}"
                f" · теги: {tags_str}\n  Источник: {src}\n  {content}"
            )
        return "\n".join(lines)

    def list_knowledge(topic: str = "") -> str:
        """Показать все записи базы знаний (или только по теме)."""
        items = _kb_load()
        if not items:
            return "[knowledge] база пуста"
        if topic.strip():
            items = [i for i in items if topic.strip().lower() in i.get("topic", "").lower()]
            if not items:
                return f"[knowledge] нет записей с темой '{topic}'"
        # Группируем по теме
        by_topic = {}
        for item in items:
            t = item.get("topic", "общее")
            by_topic.setdefault(t, []).append(item)
        lines = [f"📚 База знаний — всего {sum(len(v) for v in by_topic.values())} записей в {len(by_topic)} темах:\n"]
        for t, recs in sorted(by_topic.items()):
            lines.append(f"\n▸ {t} ({len(recs)}):")
            for r in recs[:20]:  # не больше 20 на тему чтобы не раздулось
                preview = r.get("content", "")[:100].replace("\n", " ")
                lines.append(f"  [{r['id']}] {r.get('ts','')} — {preview}...")
            if len(recs) > 20:
                lines.append(f"  ... и ещё {len(recs)-20} записей")
        return "\n".join(lines)

    def forget(record_id: str) -> str:
        """Удалить запись из базы знаний по её id."""
        items = _kb_load()
        rid = (record_id or "").strip()
        if not rid:
            return "[forget] укажи id записи (например 'k_0003')"
        before = len(items)
        items = [i for i in items if i.get("id") != rid]
        if len(items) == before:
            return f"[forget] запись с id='{rid}' не найдена"
        _kb_save(items)
        return f"✅ Запись {rid} удалена. Осталось записей: {len(items)}."

